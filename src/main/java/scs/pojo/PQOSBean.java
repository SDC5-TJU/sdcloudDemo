package scs.pojo;

public class PQOSBean {
	
	private long time;
	
	private float cachemiss;
	
	private float mbl;
	
	private float mbr;

	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}

	public float getCachemiss() {
		return cachemiss;
	}

	public void setCachemiss(float cachemiss) {
		this.cachemiss = cachemiss;
	}

	public float getMbl() {
		return mbl;
	}

	public void setMbl(float mbl) {
		this.mbl = mbl;
	}

	public float getMbr() {
		return mbr;
	}

	public void setMbr(float mbr) {
		this.mbr = mbr;
	}
	
}
